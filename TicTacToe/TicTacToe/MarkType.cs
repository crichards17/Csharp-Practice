
namespace TicTacToe
{
    /// <summary>
    /// The current value of a given cell in the game
    /// </summary>
    public enum MarkType
    {
        /// <summary>
        /// Cell has not yet been selected
        /// </summary>
        Blank,
        /// <summary>
        /// Cell is selected as an X
        /// </summary>
        X,
        /// <summary>
        /// Cell is selected as an O
        /// </summary>
        O
    }
}
